//OPERADORES ARITMÉTICOS
//adição +
var sumTwoNumbers = 5 + 3;
console.log(sumTwoNumbers);

//subtração -
var subTwoNumbers = 5 - 3;
console.log(subTwoNumbers);

//multiplicação *
var multTwoNumbers = 5 * 3;
console.log(multTwoNumbers);

//divisão /
var divTwoNumbers = 5 / 3;
console.log(divTwoNumbers);

//módulo %
console.log(8 % 3);

//incremento ++
var num = 1;
console.log(++num);

//decremento --
var num = 2;
console.log(--num);

//OPERADORES DE ATRIBUIÇÃO =
var x = 1;
var y = 2;
console.log(x+=y);
console.log(x-=y);
console.log(x*=y);
console.log(x/=y);
console.log(x%=y);

//OPERADORES DE COMPARAÇÃO
// igual ==
var a = 2;
var b = 4;
console.log(a==b);

// maior >
console.log(a>b)

// menor <
console.log(a<b);

//OPERADORES LÓGICOS
// E &&
var f = 14;
var g = 4;
var h = 5;
console.log(f > 10 && g > 2)

// OU ||
console.log(f > 14 || h > 2 )

// negação !
console.log(!h>2)


